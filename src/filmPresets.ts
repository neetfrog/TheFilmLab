// Utility functions for preset management
export const getPresetsByType = (type: FilmPreset['type']) =>
  filmPresets.filter(p => p.type === type);

export const getPresetById = (id: string) =>
  filmPresets.find(p => p.id === id);

export const getPresetsByBrand = (brand: string) =>
  filmPresets.filter(p => p.brand === brand);

export const getAllBrands = () =>
  Array.from(new Set(filmPresets.map(p => p.brand))).sort();

export const getPresetsGroupedByType = () => {
  const grouped: Record<FilmPreset['type'], FilmPreset[]> = {
    'color-negative': [],
    'bw-negative': [],
    'slide': [],
    'cinema': [],
  };
  filmPresets.forEach(p => grouped[p.type].push(p));
  return grouped;
};
