// Real film stock emulation presets
// Each preset defines color curves, grain characteristics, and tonal adjustments

export interface FilmPreset {
  id: string;
  name: string;
  brand: string;
  type: 'color-negative' | 'bw-negative' | 'slide' | 'cinema';
  iso: number;
  description: string;
  // Color adjustments
  curves: {
    r: [number, number][]; // control points for red channel curve
    g: [number, number][]; // control points for green channel curve
    b: [number, number][]; // control points for blue channel curve
  };
  // Tone mapping
  contrast: number;       // -1 to 1
  brightness: number;     // -1 to 1
  saturation: number;     // 0 to 2
  // Color cast
  shadows: [number, number, number];    // RGB tint for shadows
  midtones: [number, number, number];   // RGB tint for midtones
  highlights: [number, number, number]; // RGB tint for highlights
  // Grain
  grainAmount: number;    // 0 to 1
  grainSize: number;      // 1 to 5
  grainRoughness: number; // 0 to 1, how clumpy/organic the grain is
  // Effects
  vignette: number;       // 0 to 1
  halation: number;       // 0 to 1 (light bleed around bright areas)
  fadedBlacks: number;    // 0 to 1 (lifted black point)
  // Tone curve shape
  toeStrength: number;    // shadow roll-off 0-1
  shoulderStrength: number; // highlight roll-off 0-1
}

